public class StudentOld {

    String name;
    int age; 
    int grade;
    
    public StudentOld() {
        name = "";
        age = 0;
        grade = 0;
    }

    public StudentOld(String name, int age, int grade) {
        this.name = name;
        this.age = age;
        this.grade = grade;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public int getGrade() {
        return grade;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }

}
